#include "push_swap.h"

void	pa(t_stack_pair *p)
{
	if (p->b->len > 0)
		push(p->a, pop(p->b));
}

void	pb(t_stack_pair *p)
{
	if (p->a->len > 0)
		push(p->b, pop(p->a));
}
