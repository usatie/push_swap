#include "push_swap.h"

void	pa(t_stack *a, t_stack *b)
{
	if (b->len > 0)
		push(a, pop(b));
}

void	pb(t_stack *a, t_stack *b)
{
	if (a->len > 0)
		push(b, pop(a));
}
